#!/bin/bash
# ==============================================================================
# 腳本名稱: run_all_tests.sh
# 腳本說明: 執行全域自動化整合測試套件
# 
# 執行環境:
#   - 需要在專案根目錄下，或是透過絕對路徑呼叫此腳本。
#   - 必須確保已進入 python 虛擬環境且已安裝 pytest 等依賴套件。
# 
# 執行邏輯:
#   1. 設定 `set -e`，遇到任何測試失敗即刻中斷腳本執行。
#   2. 自動切換回專案的根目錄。
#   3. 使用 `pytest` 執行 `test/` 目錄下所有的測試腳本，並輸出詳細資訊 (-v)。
# ==============================================================================

# 遇到任何一個測試失敗就立刻中斷
set -e

# 切換到專案根目錄 (確保腳本從任何位置執行都能正確找到測試路徑)
cd "$(dirname "$0")/.."

echo "=== 執行全域自動化測試套件 ==="
pytest test/ -v
